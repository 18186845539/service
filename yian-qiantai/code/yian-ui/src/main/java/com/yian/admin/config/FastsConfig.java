package com.yian.admin.config;

public class FastsConfig
{
	private String scustomer;
	private String sKey;
	private String faststart;
	
	
	public String getScustomer() {
		return scustomer;
	}
	public void setScustomer(String scustomer) {
		this.scustomer = scustomer;
	}
	public String getsKey() {
		return sKey;
	}
	public void setsKey(String sKey) {
		this.sKey = sKey;
	}
	public String getFaststart() {
		return faststart;
	}
	public void setFaststart(String faststart) {
		this.faststart = faststart;
	}
	
	
}
